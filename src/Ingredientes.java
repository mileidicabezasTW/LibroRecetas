public class Ingredientes {

    private String nombreIngrediente;
    private double cantidaIngrediente;
    private int cantidadIngrediente;

    public Ingredientes(){

    }

    public void setNombreIngrediente(String nombreIngrediente) {
        this.nombreIngrediente = nombreIngrediente;
    }

    public String getNombreIngrediente() {
        return nombreIngrediente;
    }

    public void setCantidaIngrediente(double cantidaIngrediente) {
        this.cantidaIngrediente = cantidaIngrediente;
    }

    public double getCantidaIngrediente() {
        return cantidaIngrediente;
    }

    public void setCantidadIngrediente(int cantidadIngrediente) {
        this.cantidadIngrediente = cantidadIngrediente;
    }

    public int getCantidadIngrediente() {
        return cantidadIngrediente;
    }
}
